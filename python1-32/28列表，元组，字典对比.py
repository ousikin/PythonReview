# 列表
'''
    1.特点：保存多个元素，元素有顺序，可以通过下标找到每个元素，可以
    增加，删除，修改，查询
    2.关键字：list
    3.内置函数：
        增：append insert  extend
        删：pop clear  remove  del
        改: sort  index count

'''

# 元组
'''
     1.特点：保存多个元素，不可以增加，删除，修改，但是可查询
     2.内置函数：
     index  count
     支持下标操作，支持切片
    

'''

# 字典

'''
    1.特点：通过键值对保存元素，键时唯一，而值不唯一，可以
    增加，删除，修改，查询
    2.内置函数
        增：dict={}  dict[key]=value
        删：pop(index,默认值) popitem()  clear()
        查：items()  values() keys()
        
'''
