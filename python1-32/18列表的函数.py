# 删
''' 
    del list[::]
    remove(e) 删除列表中第一次出现的元素e,返回值是None,
    如果没有找到要删除的元素报出异常
    pop() 移除列表中的最后一个元素，返回值是要删除的那个元素
    默认删除最后一个元素，但也可以指定index删除
    clear() 删除整个列表
'''

